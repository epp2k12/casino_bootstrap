		When a member has come across a new recipe, if he feels so, he goes and shares the same with the cook. Or his wife does, too, since everyone is encourage to help make the Casino's menu the best in the city. A member could also request the cook to come up with certain recipes, for the Casino Espa&ntilde;ol is second home to all members.
